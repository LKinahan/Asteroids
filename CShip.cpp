////////////////////////////////////////////////////////////////
// Class Constructor
////////////////////////////////////////////////////////////////
CShip::CShip()
	{
	//radius of ship
	_radius = 20;
	//ship has 10 lives before removed from image
	set_lives(10);
	}

////////////////////////////////////////////////////////////////
// Class Destructor
////////////////////////////////////////////////////////////////
CShip::~CShip()
	{

	}

////////////////////////////////////////////////////////////////
// Class Draw
////////////////////////////////////////////////////////////////
//set color of ship
cv::Scalar CShip::ship = cv::Scalar(255, 204, 0);
//shape of ship to be filled circle
void CShip::draw(cv::Mat& im)
	{
	cv::circle(im, get_pos(), _radius, ship, -1);
	}
